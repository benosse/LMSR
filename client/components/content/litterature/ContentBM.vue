                    Dans le personnage historique du diplomate, on retrouve déjà une attitude proche, celle de rappeler à ses mandants qu’ils ne peuvent pas faire cavalier seul, qu’ils n’existent pas sans leur dehors. Mais le diplomate historique entre nations dans sa forme traditionnelle n’est pas le bon modèle, car il limite souvent sa pratique diplomatique à continuer la guerre par d’autres moyens, au service de son propre camp. Le diplomate “de la relation” ici portraituré est d’une autre nature, du fait de sa position au service des interdépendances.
