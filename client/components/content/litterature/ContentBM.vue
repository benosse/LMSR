                    Ce personnage du diplomate est tout ensemble un intercesseur, un traducteur interespèces et un go-between. Ce dernier n’est pas un sage supérieur qui sait mieux que les autres où sont leurs intérêts. Pas de retour du Patriarche, du jugement de Salomon. Au contraire, il reconnaît l’intelligence collective, l’intelligence des acteurs, le fait que ce sont eux qui savent ce qu’ils font, et les lignes de force de la pratique et de la vie. Il est à hauteur de vivants. Mais sa bizarrerie est liée à sa position “entre” : elle est “positionnelle-relationnelle(1)”. 
                    </p>
                    <p>
                    C’est-à-dire qu’elle est liée à sa position contextuelle dans un champ de relations. S’il sait qu’il n’y a pas de déficit d’intelligence chez les acteurs, les loups, les brebis, les bergers et les écolos, il reconnaît en revanche la dimension positionnelle de la conception qu’ils se font de leurs propres intérêts : c’est-à-dire que chaque camp a spontanément tendance à négliger ses interdépendances les moins évidentes avec les autres camps, à se croire auto-extrait des interdépendances. Si le berger a la garde des moutons, le diplomate a la garde des interdépendances, c’est ce qui accapare son attention ontologique. 
					</p>
